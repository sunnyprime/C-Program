#include<stdio.h>  //Header of c programs

int main()       //Main Function of the programs
{
  int a=10;     //integer
  float b=20.05;    //float
  char c='R';       //Character
  char s[]="Ram";   //String
  printf("Hello World!\n"); //printf is a function which is used to print the character on the screen
                            //\n is definde for new lines

  printf("Integer is:%d\n",a); //%d is used for printing integer
  printf("Float is:%f\n",b);  //%f is used for printng float
  printf("Character is:%c\n",c); //%c is used for printing character
  printf("String is: %s\n", s); //%s is used for printing string
}
